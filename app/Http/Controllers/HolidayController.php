    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $holiday = new Holiday();
        $holiday->holiday_title = $request->holiday_title;
        $holiday->holiday_date = $request->holiday_date;
        $holiday->holiday_description = $request->holiday_description;
        $holiday->created_by = \Auth::user()->id;;
        $holiday->save();
        return redirect()->back()->with('alert', 'The Holiday Created Successfully!')->with('alert-type', 'alert-success');
    }

